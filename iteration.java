public class iteration {
	public static void main(String[] args)
	{
		int a,b;
		int c = 5;//change the value of c to increase the size of cuboid
		for (a=1;a<=c;a++)
		{
			for (b=1;b<=c;b++)
			{
				System.out.print("*");
				
			}
			
			System.out.println();
		}
		
			
		
	}
	
	
}
