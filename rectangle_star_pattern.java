package demo;

public class rectangle_star_pattern {
	public static void main(String[] args)
	{
		int a,b;
		int row = 5;//change the value of c to increase the size of cuboid
	
		for (a=1;a<=row;a++)
		{
			for (b=1;b<=row;b++)
			{
				System.out.print("*");
				
			}
			
			System.out.println();
		}		
	}
}
